#Faça um programa que peça dois números, realize as principais operações soma, subtração,
#multiplicação, divisão.

numero1 = float(input('Digite aqui o primeiro número: '))
numero2 = float(input('Digite aqui o segundo número: '))

soma = numero1+numero2
subtracao = numero1-numero2
multiplicacao = numero1*numero2
divisao = numero1/numero2

print(f'O resultado da soma foi {soma}!')
print(f'O resultado da subtração foi {subtracao}!')
print(f'O resultado da multiplicação foi {multiplicacao}!')
print(f'O resultado da divisão foi {divisao}!')